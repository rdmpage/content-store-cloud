		}
		
		$source->content_filename = $config['tmp'] . '/' . $source->content_filename;
			
		if (1)
		{
			$source->url = str_replace("'", "%27", $source->url);
				
			// -k to override bad certificates
			// --max-time for slow connections
			$command = "curl --max-time 1024 -k -L -sS"
			. " --user-agent \"Mozilla/5.0 (Linux; Android 10; SM-G996U Build/QP1A.190711.020; wv) AppleWebKit/537.36 (KHTML, like Gecko) Version/4.0 Mobile Safari/537.36\""
			. " -o " . escapeshellarg($source->content_filename)
			. " " . escapeshellarg($source->url)
			. " >/dev/null 2>&1";

			//$command = "curl --max-time 1024 -k -L  -o '$source->content_filename' '$source->url'";
		}
		else
		{
			$command = "curl_chrome116 -k -L  -o '$source->content_filename' '$source->url'";

			
			$command = "'/Applications/Google Chrome.app/Contents/MacOS/Google Chrome'"
				. "  --headless --disable-gpu --dump-dom"
				. " --user-agent=\"Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/60.0.3112.50 Safari/537.36\""
				. " '" . $source->url . "'"
				. ' > ' . $source->content_filename;
			
		}
		echo $command . "\n";
		
		system($command, $result_code);
		
		// check OK
		if ($result_code == 0)
		{
			$source->url = str_replace("%27", "'", $source->url);
			store_pdf($source, $parse_pdf, $debug);
		}
		
		$rand = rand(1000000, 5000000);
		echo "\n-- ...sleeping for " . round(($rand / 1000000),2) . ' seconds' . "\n\n";
		usleep($rand);
	}
}

