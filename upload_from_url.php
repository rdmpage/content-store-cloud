			store_pdf($source, $parse_pdf, $debug);
		}
		
		$rand = rand(1000000, 5000000);
		echo "\n-- ...sleeping for " . round(($rand / 1000000),2) . ' seconds' . "\n\n";
		usleep($rand);
	}
}

